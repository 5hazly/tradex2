export function generateEquityCurve(): EquityCurvePoint[] {
  const points: EquityCurvePoint[] = [];
  let equity = 30000;
  const now = new Date("2025-01-15T00:00:00Z");

  const dailyChanges = [
    0.8, -0.3, 1.2, 0.5, -0.8, 1.5, 2.1, -0.4, 0.9, 1.3,
    -1.2, 0.6, 1.8, -0.5, 0.3, 1.1, -0.9, 1.4, 0.7, -0.2,
    1.6, 0.4, -1.1, 0.8, 1.9, -0.6, 1.0, 0.5, -0.3, 0.7,
  ];

  let peak = equity;
  let maxDd = 0;

  for (let i = 29; i >= 0; i--) {
    const date = new Date(now);
    date.setDate(date.getDate() - i);
    const change = dailyChanges[29 - i];
    const pnl = equity * (change / 100);
    equity += pnl;

    if (equity > peak) peak = equity;
    const dd = ((peak - equity) / peak) * 100;
    if (dd > maxDd) maxDd = dd;

    const trades = Math.floor(Math.random() * 5) + 1;

    points.push({
      date: date.toISOString().split("T")[0],
      equity: Math.round(equity * 100) / 100,
      pnl: Math.round(pnl * 100) / 100,
      drawdown: Math.round(dd * 100) / 100,
      trades,
    });
  }

  return points;
}

